                <p>En la actualidad, el futuro profesional de la danza pasa irremediablemente por introducirse en el mundo de los musicales, la televisión o el cine. Para ello, <b>no basta con practicar una sola disciplina</b> sino que <b>requiere de una formación integral</b>.</p>
                <p><b>El musical es el arte escénico por excelencia, el más completo</b>, en el que la acción se desenvuelve en combinación de música, canción, diálogo y baile. Se representa en grandes escenarios como los teatros de West End (Londres) o en Broadway (New York), principales sedes, seguido de Argentina, Australia, Canadá, España y México. Solo en España se representaron durante este último año más de 24 grandes producciones musicales. Mama mía; The Book ok Mormón; Matilde The musical; El rey León; Cabaret; Viva Broadway; Dunas; Pues me muero; El gato con botas; Peter Pan; Goodbye España, Dúo la Inegalité; Carmen y The Hole, son alguno de los ejemplos más actuales.</p>
                <p>Bailaran Art Academy es la única escuela en Aragón que imparte los conocimientos y la variedad de estilos de danza necesarios para una formación completa en artes escénicas, ya que cuenta con profesionales con experiencia profesional televisiva y músical. Con éstos el alumno trabaja durante todo un curso lectivo, la <b>técnica vocal</b> y <b>canto</b>, la <b>interpretación</b>, la <b>danza</b> y el <b>casting</b>.</p>
                <p>La formación en musical está dirigida a cualquier persona que quiera introducirse en el mundo de los musicales, televisión o cine. <b>Disponemos de cursos adaptados a todos los niveles y edades</b>, tanto para los bailarines que quieren iniciarse en el mundo del espectáculo como para los alumnos que, simplemente desean aprender, divertirse y mantenerse en forma.</p>
